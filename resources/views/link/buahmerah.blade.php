<p>
Pandanus Conoideus is a plant that grows in the mountain regions of Papua, Indonesia. It bears a miraculous fruit which the Papuans call "kuansu" or "buah merah" which literally means a "red fruit" in Indonesia.</p>
<p> 
It can grow in huge sizes around 100 cm in length, 18 cm in diameter length and weighs about 7.5 kg. The fruit has a  blunt edge and cylindrical shape. <br><br>
It has known several medicinal applications which the natives use and was also  believed to be a good supplement. 
</p>
